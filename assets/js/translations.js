// Language management system
let currentLanguage = localStorage.getItem('preferredLanguage') || 'en';

function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    updatePageContent();
    updateLanguageSelector();
    
    // Update HTML dir attribute for RTL languages
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', lang);
}

function updatePageContent() {
    const t = translations[currentLanguage];
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });
    
    // Update image data for coverflow
    if (typeof imageData !== 'undefined') {
        imageData.forEach((item, index) => {
            item.title = t[`home_title_${index + 1}`];
            item.description = t[`home_desc_${index + 1}`];
        });
        
        // Update current display
        if (typeof updateCoverflow === 'function') {
            updateCoverflow();
        }
    }
    
    // Update language toggle text
    const currentLangText = document.getElementById('currentLangText');
    if (currentLangText) {
        currentLangText.textContent = t.lang_name;
    }
}

function updateLanguageSelector() {
    document.querySelectorAll('.lang-option').forEach(option => {
        option.classList.toggle('active', option.dataset.lang === currentLanguage);
    });
}

// Toggle language dropdown
function toggleLanguageDropdown() {
    const selector = document.getElementById('languageSelector');
    selector.classList.toggle('active');
}

// Close dropdown when clicking outside
document.addEventListener('click', (e) => {
    const selector = document.getElementById('languageSelector');
    const toggle = document.getElementById('langToggle');
    
    if (selector && !selector.contains(e.target)) {
        selector.classList.remove('active');
    }
});

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    updatePageContent();
    updateLanguageSelector();
    
    // Set up language toggle click handler
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            toggleLanguageDropdown();
        });
    }
});